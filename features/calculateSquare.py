# Add two numbers
def add(a, b):
    return a + b


# Add three numbers
def add1(a, b, c):
    return a + b + c


# Add four numbers
def add2(a, b, c, d):
    return a + b + c + d


# Add four numbers
def multiply(a, b):
    return a * b


print(add(5, 3))
print(add1(5, 3, 4))
print(add2(5, 3, 4, 5))
print(multiply(2, 2))
